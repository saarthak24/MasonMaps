/**
 * Enum for representing the various types of users.
 */
public enum UserType {
    /**
     * New and prospective on-campus students.
     */
    newOnCampusStudent,
    /**
     * New and prospective off-campus students.
     */
    newOffCampusStudent,
    /**
     * Current/established on-campus students.
     */
    currentOnCampusStudent,
    /**
     * Current/established off-campus students.
     */
    currentOffCampusStudent,
    /**
     * New faculty members.
     */
    newFaculty,
    /**
     * Current/established faculty members.
     */
    currentFaculty
}
